package algorithms;

import java.util.Scanner;
public class MultiplicationTable {
//github link: https://github.com/FrancescoSciab/CA2-Programming-and-Math.git
    
    public static void main(String[] args) {
        Scanner scanner = new Scanner(System.in);
        
        // Input: Size of the multiplication table
        System.out.print("Enter size of multiplication table: ");
        int n = scanner.nextInt();
        
        // Creating a 2D array to hold the multiplication table
        int[][] table = new int[n][n];
        
        // Populating the 2D array with multiplication values
        for (int i = 0; i < n; i++) {
            for (int j = 0; j < n; j++) {
                table[i][j] = (i + 1) * (j + 1);
            }
        }
        // Displaying the multiplication table in matrix form
        System.out.println("Multiplication Table:");
        for (int i = 0; i < n; i++) {
            for (int j = 0; j < n; j++) {
                System.out.printf("%4d", table[i][j]); // Aligning values for a cleaner output
            }
            System.out.println(); // Move to the next row
        }

        scanner.close();
    
    }
    
}
